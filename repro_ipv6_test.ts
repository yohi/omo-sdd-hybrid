import { mask } from './.opencode/lib/pii-masker';
// パターンの順序を調整：( ...:){1,7}: を最後に持ってくる
const IPV6_REGEX = /(?:(?:[0-9a-fA-F]{1,4}:){7}[0-9a-fA-F]{1,4}|(?:[0-9a-fA-F]{1,4}:){1,6}:[0-9a-fA-F]{1,4}|(?:[0-9a-fA-F]{1,4}:){1,5}(?::[0-9a-fA-F]{1,4}){1,2}|(?:[0-9a-fA-F]{1,4}:){1,4}(?::[0-9a-fA-F]{1,4}){1,3}|(?:[0-9a-fA-F]{1,4}:){1,3}(?::[0-9a-fA-F]{1,4}){1,4}|(?:[0-9a-fA-F]{1,4}:){1,2}(?::[0-9a-fA-F]{1,4}){1,5}|[0-9a-fA-F]{1,4}:(?:(?::[0-9a-fA-F]{1,4}){1,6})|:(?:(?::[0-9a-fA-F]{1,4}){1,7}|:)|fe80:(?::[0-9a-fA-F]{0,4}){0,4}%[0-9a-zA-Z]{1,}|::(?:ffff(?::0{1,4}){0,1}:){0,1}(?:(?:25[0-5]|(?:2[0-4]|1{0,1}[0-9]){0,1}[0-9])\.){3,3}(?:25[0-5]|(?:2[0-4]|1{0,1}[0-9]){0,1}[0-9])|(?:[0-9a-fA-F]{1,4}:){1,4}:(?:(?:25[0-5]|(?:2[0-4]|1{0,1}[0-9]){0,1}[0-9])\.){3,3}(?:25[0-5]|(?:2[0-4]|1{0,1}[0-9]){0,1}[0-9])|(?:[0-9a-fA-F]{1,4}:){1,7}:)/g;
const testStr = "Testing IPv6: 2001:db8::1 in a sentence.";
const match = testStr.match(IPV6_REGEX);
console.log("Match:", match);
if (match && match.includes("2001:db8::1")) {
  console.log("SUCCESS: IP matched correctly.");
} else {
  console.log("FAILURE: IP did not match fully.");
  process.exit(1);
}

const masked = mask(testStr);
console.log("Masked:", masked);
if (masked.includes("<REDACTED:IPV6>")) {
  console.log("SUCCESS: mask() function worked correctly.");
} else {
  console.log("FAILURE: mask() function failed.");
  process.exit(1);
}
